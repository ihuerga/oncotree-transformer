package org.mskcc.cmo.model;

public class OncotreeConcept {
	
	private String primary;
	private String secondary;
	private String terciary;
	private String quaternary;
	private String quinternary;
	private String mainType;
	private String color;
	private String nci;
	private String umls;
	private String nccn;
	private int idx;
	
	private String id;
	
	public OncotreeConcept(){
		
	}

	public String getPrimary() {
		return primary;
	}

	public void setPrimary(String primary) {
		this.primary = primary;
	}

	public String getSecondary() {
		return secondary;
	}

	public void setSecondary(String secondary) {
		this.secondary = secondary;
	}

	public String getTerciary() {
		return terciary;
	}

	public void setTerciary(String terciary) {
		this.terciary = terciary;
	}

	public String getQuaternary() {
		return quaternary;
	}

	public void setQuaternary(String quaternary) {
		this.quaternary = quaternary;
	}

	public String getQuinternary() {
		return quinternary;
	}

	public void setQuinternary(String quinternary) {
		this.quinternary = quinternary;
	}

	public String getMainType() {
		return mainType;
	}

	public void setMainType(String mainType) {
		this.mainType = mainType;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public String getNci() {
		return nci;
	}

	public void setNci(String nci) {
		this.nci = nci;
	}

	public String getUmls() {
		return umls;
	}

	public void setUmls(String umls) {
		this.umls = umls;
	}

	public String getNccn() {
		return nccn;
	}

	public void setNccn(String nccn) {
		this.nccn = nccn;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getIdx() {
		return idx;
	}

	public void setIdx(int idx) {
		this.idx = idx;
	}
	
	
	

}
